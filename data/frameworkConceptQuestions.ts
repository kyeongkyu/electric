const profilesBySubject: Record<string, Record<string, ChapterProfile>> = {
  [ELEC_THEORY_FRAMEWORK_ID]: elecTheoryChapterProfiles,
  [CIRCUIT_THEORY_FRAMEWORK_ID]: circuitTheoryChapterProfiles,
};

const buildLessonConceptQuestions = (): Record<string, Quiz[]> => {
  const entries: Array<[string, Quiz[]]> = [];

  for (const subjectId of [ELEC_THEORY_FRAMEWORK_ID, CIRCUIT_THEORY_FRAMEWORK_ID]) {
    const subject = curriculum.find(item => item.id === subjectId);
    const profiles = profilesBySubject[subjectId];
    if (!subject) continue;

    for (const chapter of subject.chapters) {
      const profile = profiles[chapter.id];
      if (!profile) continue;
      const chapterTitle = cleanTitle(chapter.title);

      for (const lesson of chapter.lessons) {
        entries.push([
          lesson.id,
          [
            q(
              `${lesson.id}-q1`,
              `"${lesson.title}"에서 가장 먼저 확인해야 할 핵심 관점은?`,
              [
                `${chapterTitle}의 큰 흐름 안에서 ${lesson.title}의 역할과 물리적 의미를 연결한다`,
                `${lesson.title}의 이름만 외우고 다른 개념과의 관계는 보지 않는다`,
                `계산식이 나오면 단위와 기준 방향은 확인하지 않아도 된다`,
                `실제 물리 의미보다 그림의 모양만 기억하면 충분하다`,
              ],
              0,
              `${lesson.title}은 ${profile.focus}과 연결해 이해해야 합니다. 특히 ${profile.commonMistake}을 피하는 것이 중요합니다.`,
            ),
          ],
        ]);
      }
    }
  }

  return Object.fromEntries(entries);
};

export const frameworkLessonConceptQuestions: Record<string, Quiz[]> = buildLessonConceptQuestions();

